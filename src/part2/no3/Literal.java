package part2.no3;

// 기본형 데이터 타입
public class Literal {
	public static void main(String[] args) {
		/*
		 * 데이터 타입 종류 : 정수 / 실수 / 문자 / 논리
		 *
		 * 리터럴 = 값 자체
		 * 리터럴을 특정타입 변수에 대입할수있다
		 */
		
		boolean isFun = true;
		char c = 'S';	// 문자 하나만 사용할수있음.
		char h = '한';	// 한글도 가능!
		
		int num = 10;
		
		long big = 1000000000L;	// 대문자 또는 소문자 L을 넣어서 long타입임을 표현
		
		float f = 22.1f; // 대문자 또는 소문자 F을 넣어서 float타입임을 표현
		
		double d = 3.1415923321;
		
	}
}
